void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
	CAN_RxHeaderTypeDef hdr; //Variable to store header of incoming CAN message
    CANMessage_t msg; //Variable to store message in our own format

    //Attempt to read incoming message from FIFO0
	if (HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &hdr, msg.data) != HAL_OK )
	{
		//If reading fails call HAL error handler
		Error_Handler();
	}
	else
	{
		//If reading succeeds, copy important info from HAL header into our own structure
		msg.id = hdr.StdId;
		msg.dlc = hdr.DLC;

		//Declare flag to tell FreeRTOS whether a higher priority task has woken
		BaseType_t hpw = pdFALSE;
		//Put message in the RX queue from ISR
		xQueueSendFromISR(xCanRx, &msg, &hpw);
		//If higher priority task has woken switch to it immediately.
		portYIELD_FROM_ISR(hpw);

	}
}

